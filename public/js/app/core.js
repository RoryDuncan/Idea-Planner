var core, testData;

core = new Backbone.Marionette.Application();

core.addRegions({
  AppContainer: ".app-container",
  ListOfProjects: ".app-projects-list",
  ListedHeader: ".app-projects-header",
  ListedFooter: ".app-projects-footer"
});

core.addInitializer(function() {
  var ProjectList, mainView;
  ProjectList = App.View.ProjectList;
  mainView = new ProjectList;
  return core.AppContainer.show(mainView);
});

testData = function(numberOfModels) {
  var m, modelList, x, _i;
  console.warn("Model Creation");
  modelList = [];
  for (x = _i = 0; 0 <= numberOfModels ? _i <= numberOfModels : _i >= numberOfModels; x = 0 <= numberOfModels ? ++_i : --_i) {
    console.log(x);
    m = new App.Model({
      name: "Model " + x,
      description: "just wow " + x
    });
    modelList.push(m);
  }
  return App.projects.add(modelList);
};

App.core = core;

$(document).ready(function() {
  var response;
  App.projects.reset();
  console.log("Starting App");
  App.core.start();
  console.log("Adding test models");
  console.log(window.App);
  console.log(App.projects);
  response = App.projects.sync("read", App.projects, {
    success: function(a, b, c) {
      console.log("success!");
      return console.log(a, b, c);
    },
    error: function(a, b, c) {
      console.log("not a sucess..");
      return console.log(a, b, c);
    }
  });
  return console.log("waiting..");
});

