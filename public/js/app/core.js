var core;

core = new Backbone.Marionette.Application();

core.addRegions({
  AppContainer: ".app-container",
  BreadCrumbs: ".app-breadcrumbs",
  ComponentSelectorContainer: ".app-modules-container"
});

core.addInitializer(function() {
  var ProjectList, mainView;
  ProjectList = App.View.ProjectList;
  mainView = new ProjectList;
  core.AppContainer.show(mainView);
  Backbone.history.start({
    pushState: false,
    root: "demo.html#"
  });
  return App.router.navigate("app/", {
    trigger: true
  });
});

App.core = core;

$(document).ready(function() {
  return App.core.start();
});

